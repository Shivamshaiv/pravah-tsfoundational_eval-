# === Custom Loss Function ===
class CustomLast24hMAELoss(nn.Module):
    def __init__(self):
        super().__init__()
    def forward(self, y_pred, y_true):
        # For 15-minute data, last 24h = 96 time steps (24 * 4)
        return torch.mean(torch.abs(y_true[:, -96:, :] - y_pred[:, -96:, :]))

